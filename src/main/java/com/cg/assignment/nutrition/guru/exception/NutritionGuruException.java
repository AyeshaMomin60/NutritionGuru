package com.cg.assignment.nutrition.guru.exception;


public class NutritionGuruException  extends Exception{

	public NutritionGuruException() {
		super();
		
	}

	public NutritionGuruException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public NutritionGuruException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public NutritionGuruException(String message) {
		super(message);
		
	}

	public NutritionGuruException(Throwable cause) {
		super(cause);
		
	}

}
